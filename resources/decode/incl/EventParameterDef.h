#define TIME_SET_TIME(h,m,s,ms) (0x80000000 | (h << 22) | (m << 16) | (s << 10) | ms)
#define TIME_SET_VALID(n)   (n = n | 0x8000000)
#define TIME_SET_INVALID(n) (n = n & (~0x8000000))
#define TIME_GET_HOUR(n)    ((n >> 22) & 0x1F)
#define TIME_GET_MIN(n)     ((n >> 16) & 0x3F)
#define TIME_GET_SEC(n)     ((n >> 10) & 0x3F)
#define TIME_GET_MSEC(n)    (n  & 0x3FF)
#define TIME_GET_VALID(n)   (n & 0x80000000)


